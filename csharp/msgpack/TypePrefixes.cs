namespace msgpack
{
	public enum TypePrefixes : byte
	{
		PositiveFixNum = 0x00, // 0x00 - 0x7f
		NegativeFixNum = 0xe0, // 0xe0 - 0xff

		Nil = 0xc0,
		False = 0xc2,
		True = 0xc3,
		Float = 0xca,
		Double = 0xcb,
		UInt8 = 0xcc,
		UInt16 = 0xcd,
		UInt32 = 0xce,
		UInt64 = 0xcf,
		Int8 = 0xd0,
		Int16 = 0xd1,
		Int32 = 0xd2,
		Int64 = 0xd3,
		Raw16 = 0xda,
		Raw32 = 0xdb,
		Array16 = 0xdc,
		Array32 = 0xdd,
		Map16 = 0xde,
		Map32 = 0xdf,

		FixRaw = 0xa0,   // 0xa0 - 0xbf
		FixArray = 0x90, // 0x90 - 0x9f
		FixMap = 0x80,   // 0x80 - 0x8f
	}
}
